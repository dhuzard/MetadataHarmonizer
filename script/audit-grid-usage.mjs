import { mkdirSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { GRID_ADAPTER_CONTRACT } from './grid-engine-audit-config.mjs';
import {
  collectExporterLines,
  collectHotLines,
  pluralize,
} from './grid-engine-audit-lib.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');
const outputDir = path.join(repoRoot, 'docs', 'grid-engine');
const markdownPath = path.join(outputDir, 'hot-surface-audit.md');
const jsonPath = path.join(outputDir, 'hot-surface-audit.json');
const hotLines = collectHotLines(repoRoot);
const getTrimmedDataLines = collectExporterLines(repoRoot);

const methodSummary = new Map();
for (const entry of hotLines) {
  if (!methodSummary.has(entry.method)) {
    methodSummary.set(entry.method, []);
  }
  methodSummary.get(entry.method).push({
    file: entry.file,
    line: entry.line,
  });
}

const sortedMethodSummary = [...methodSummary.entries()]
  .map(([method, locations]) => ({
    method,
    count: locations.length,
    locations: locations.sort((a, b) =>
      `${a.file}:${a.line}`.localeCompare(`${b.file}:${b.line}`)
    ),
  }))
  .sort((a, b) => b.count - a.count || a.method.localeCompare(b.method));

const bucketSummary = [
  ...hotLines
    .reduce((acc, entry) => {
      if (!acc.has(entry.bucket)) {
        acc.set(entry.bucket, 0);
      }
      acc.set(entry.bucket, acc.get(entry.bucket) + 1);
      return acc;
    }, new Map())
    .entries(),
]
  .map(([bucket, count]) => ({ bucket, count }))
  .sort((a, b) => b.count - a.count || a.bucket.localeCompare(b.bucket));

const directHotByFile = hotLines.reduce((acc, entry) => {
  if (!acc[entry.file]) {
    acc[entry.file] = [];
  }
  acc[entry.file].push({
    line: entry.line,
    method: entry.method,
    source: entry.source,
  });
  return acc;
}, {});

const sortedFiles = Object.keys(directHotByFile).sort();
for (const file of sortedFiles) {
  directHotByFile[file].sort((a, b) => a.line - b.line);
}

const audit = {
  summary: {
    directHotCallsites: hotLines.length,
    methods: sortedMethodSummary.length,
    buckets: bucketSummary.length,
    exporterGetTrimmedDataCalls: getTrimmedDataLines.length,
  },
  buckets: bucketSummary,
  methods: sortedMethodSummary,
  files: sortedFiles.map((file) => ({
    file,
    callsites: directHotByFile[file],
  })),
  exporterGetTrimmedDataCalls: getTrimmedDataLines,
};

const methodTable = sortedMethodSummary
  .map(({ method, count }) => `| \`${method}\` | ${count} |`)
  .join('\n');

const bucketTable = bucketSummary
  .map(({ bucket, count }) => `| ${bucket} | ${count} |`)
  .join('\n');

const fileSections = sortedFiles
  .map((file) => {
    const rows = directHotByFile[file]
      .map(
        ({ line, method, bucket, source }) =>
          `- L${line} [${bucket}] \`${method}\`: \`${source.replace(
            /`/g,
            '\\`'
          )}\``
      )
      .join('\n');
    return `### \`${file}\`\n${rows}`;
  })
  .join('\n\n');

const exporterLines = getTrimmedDataLines
  .map(({ file, line, source }) => `- \`${file}:${line}\` \`${source}\``)
  .join('\n');

const markdown = `# Handsontable Surface Audit

Generated by \`node script/audit-grid-usage.mjs\`.

## Summary

- ${pluralize('direct .hot callsite', hotLines.length)}
- ${pluralize(
  'distinct direct .hot method/property',
  sortedMethodSummary.length
)}
- ${pluralize('behavior bucket', bucketSummary.length)}
- ${pluralize(
  'template exporter getTrimmedData() callsite',
  getTrimmedDataLines.length
)}

## Behavior Buckets

| Bucket | Count |
| --- | ---: |
${bucketTable}

## Direct \`.hot\` Method Summary

| Method / Property | Count |
| --- | ---: |
${methodTable}

## Direct \`.hot\` Calls By File

${fileSections}

## Exporter Read Surface

${exporterLines}

## Minimal Adapter Contract Inputs

${GRID_ADAPTER_CONTRACT.map(
  ({ name, details }) =>
    `### ${name}\n${details.map((detail) => `- ${detail}`).join('\n')}`
).join('\n\n')}
`;

mkdirSync(outputDir, { recursive: true });
writeFileSync(jsonPath, `${JSON.stringify(audit, null, 2)}\n`);
writeFileSync(markdownPath, `${markdown}\n`);

console.log(`Wrote ${path.relative(repoRoot, markdownPath)}`);
console.log(`Wrote ${path.relative(repoRoot, jsonPath)}`);
